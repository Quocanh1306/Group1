    private void display() {
        int row = tb_sv.getSelectedRow();
        txt_msv.setText((String) tb_sv.getValueAt(row, 0));
        txt_hoten.setText((String) tb_sv.getValueAt(row, 1));
        txt_email.setText((String) tb_sv.getValueAt(row, 2));
        txt_sdt.setText((String) tb_sv.getValueAt(row, 3));
        if (tb_sv.getValueAt(row, 4).equals("Nam")) {
            rd_nam.setSelected(true);
        } else {
            rd_nu.setSelected(true);
        }
        txt_diachi.setText((String) tb_sv.getValueAt(row, 5));
        String path = (String) tb_sv.getValueAt(row, 6);
        File file = new File(path);
        int imgW = jlb_Img.getWidth();
        int imgH = jlb_Img.getHeight();
        Image img = new ImageIcon(file.getAbsolutePath())
                .getImage()
                .getScaledInstance(imgW, imgH, Image.SCALE_DEFAULT);
        ImageIcon icon = new ImageIcon(img);
        jlb_Img.setIcon(icon);
        jlb_Img.setHorizontalAlignment(JLabel.CENTER);
        jlb_Img.setVerticalAlignment(JLabel.CENTER);
        jlb_Img.setName(file.getAbsolutePath());
        txt_msv.setEditable(false);
    }

    private void column() {
        ArrayList<String> cl = new ArrayList();
        cl.add("Mã");
        cl.add("Họ tên");
        cl.add("Email");
        cl.add("Số Đt");
        cl.add("Giới tính");
        cl.add("Địa chỉ");
        cl.add("Hình");
        defaultTableModel = (DefaultTableModel) tb_sv.getModel();
        defaultTableModel.setColumnCount(0);
        for (String string : cl) {
            defaultTableModel.addColumn(string);
        }
    }

    private void fillToTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tb_sv.getModel();
        tableModel.setRowCount(0);
        List<Student> students = studentService.getListStudent();
        if (!students.isEmpty()) {
            for (int i = 0; i < students.size(); i++) {
                String[] data = new String[7];
                data[0] = students.get(i).getStudentId();
                data[1] = students.get(i).getStudentName();
                data[2] = students.get(i).getEmail();
                data[3] = students.get(i).getPhoneNumber();
                if (students.get(i).getSex().equals(Boolean.TRUE)) {
                    data[4] = "Nam";
                } else {
                    data[4] = "Nữ";
                }
                data[5] = students.get(i).getAddress();
                data[6] = students.get(i).getImage();
                tableModel.addRow(data);
            }
            tb_sv.setModel(tableModel);
            tableModel.fireTableDataChanged();
        }
    }

