    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        Student student = new Student();
        student.setStudentId(txt_msv.getText());
        student.setStudentName(txt_hoten.getText());
        student.setEmail(txt_email.getText());
        student.setPhoneNumber(txt_sdt.getText());
        if (rd_nam.isSelected()) {
            student.setSex(Boolean.TRUE);
        } else {
            student.setSex(Boolean.FALSE);
        }
        student.setAddress(txt_diachi.getText());
        student.setImage(jlb_Img.getName());
        try {
            studentService.addStu(student);
            clearFormData();
            fillToTable();
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
        } catch (RuntimeException re) {
            JOptionPane.showMessageDialog(this, re.getMessage(),
                    Constant.POPUP_HEADER, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_themActionPerformed

    private void ImgPanelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ImgPanelMouseClicked
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            int imgW = jlb_Img.getWidth();
            int imgH = jlb_Img.getHeight();
            ImageIcon icon = new ImageIcon(new ImageIcon(selectedFile.getAbsolutePath()).getImage().getScaledInstance(imgW, imgH, Image.SCALE_DEFAULT));
            jlb_Img.setName(selectedFile.getAbsolutePath());
            jlb_Img.setIcon(icon);
            jlb_Img.setHorizontalAlignment(JLabel.CENTER);
            jlb_Img.setVerticalAlignment(JLabel.CENTER);
            System.out.println("Selected file: " + selectedFile.getAbsolutePath());
        }
    }//GEN-LAST:event_ImgPanelMouseClicked

