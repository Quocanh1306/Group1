    private void fillTable(List<testt> listTest) {
        defaultTableModel.setRowCount(0);
        for (testt testt : list) {
            defaultTableModel.addRow(
                    new Object[]{testt.getId(), testt.getNamBh(), testt.getMoTa(), testt.getsLton(), testt.getGiaNhap(), testt.getsLton() >= 900 ? "Nhiều SP" : "Ít SP"
                    });
        }
    }

