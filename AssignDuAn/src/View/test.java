    private ChiTietSanPham getData (){
        ChiTietSanPham ch = new ChiTietSanPham();
        ch.setNamBH(Integer.parseInt(txtNamBh.getText()));
        ch.setMoTa(txtMota.getText());
        ch.setSoLuong(Integer.parseInt(txtSlt.getText()));
        ch.setGiaNhap(Double.parseDouble(txtGiaNhap.getText()));
        return ch;
    }
